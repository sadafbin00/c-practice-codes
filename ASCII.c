int main()
{
    char ch;
    printf("\nEnter any character =");
    scanf("%c",&ch);
    printf("\nASCII Value for this character = %d\n",ch);

    int num;
    printf("\nEnter any ASCII value =");
    scanf("%d",&num);
    printf("\nCharacter of this value = %c\n",num);
    return 0;
}
